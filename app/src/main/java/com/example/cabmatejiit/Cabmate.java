package com.example.cabmatejiit;

class Cabmate {

    String phone, name;

    public Cabmate() {
    }

    public Cabmate(String phone, String name) {
        this.phone = phone;
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
